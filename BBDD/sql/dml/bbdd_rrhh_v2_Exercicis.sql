USE rrhh;

-- ------------------------------------------------------------------------------------
-- Exercici 1
-- ------------------------------------------------------------------------------------
SELECT *
FROM empleats;

-- ------------------------------------------------------------------------------------
-- Exercici 2
-- ------------------------------------------------------------------------------------
SELECT cognoms
FROM empleats;

-- ------------------------------------------------------------------------------------
-- Exercici 3
-- ------------------------------------------------------------------------------------
SELECT DISTINCT cognoms, nom
FROM empleats;

-- ------------------------------------------------------------------------------------
-- Exercici 4
-- ------------------------------------------------------------------------------------
SELECT nom, cognoms
FROM empleats;

-- ------------------------------------------------------------------------------------
-- Exercici 5: Nom complet combinant cognoms i nom
-- ------------------------------------------------------------------------------------
SELECT CONCAT(cognoms, ', ', nom) AS nom_complet
FROM empleats;

-- ------------------------------------------------------------------------------------
-- Exercici 6: Conversió a majúscules i minúscules
-- ------------------------------------------------------------------------------------
SELECT UPPER(nom) AS nom_majuscules, LOWER(nom) AS nom_minuscules
FROM empleats;

-- ------------------------------------------------------------------------------------
-- Exercici 7: Extracció de part dels cognoms
-- ------------------------------------------------------------------------------------
SELECT LEFT(cognoms, 6)
FROM empleats;

-- ------------------------------------------------------------------------------------
-- Exercici 8: Longitud dels cognoms
-- ------------------------------------------------------------------------------------
SELECT cognoms, LENGTH(cognoms) AS longitud
FROM empleats
HAVING longitud > 6;

-- ------------------------------------------------------------------------------------
-- Exercici 9: Substitució de caracters
-- ------------------------------------------------------------------------------------
SELECT REPLACE(cognoms, 'a', 'e') AS cognoms_amb_e
FROM empleats
ORDER BY cognoms_amb_e;

-- ------------------------------------------------------------------------------------
-- Exercici 10: Filtrat per substring
-- ------------------------------------------------------------------------------------
SELECT *
FROM empleats
WHERE SUBSTRING(cognoms, 2, 2) = 'a';

-- ------------------------------------------------------------------------------------
-- Exercici 11: Càlcul de nou salari (15% augment)
-- ------------------------------------------------------------------------------------
SELECT empleat_id, cognoms, (salari * 0.15) AS nou_salari
FROM empleats;
